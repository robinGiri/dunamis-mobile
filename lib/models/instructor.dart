class Instructor {
  final String id;
  final String name;
  final String role;
  final int coursesCount;
  final double rating;
  final int followers;
  final String imageUrl;

  Instructor({
    required this.id,
    required this.name,
    required this.role,
    required this.coursesCount,
    required this.rating,
    required this.followers,
    required this.imageUrl,
  });
}
