class Course {
  final String id;
  final String title;
  final String category;
  final int price;
  final double rating;
  final int reviews;
  final String imageUrl;

  Course({
    required this.id,
    required this.title,
    required this.category,
    required this.price,
    required this.rating,
    required this.reviews,
    required this.imageUrl,
  });
}
